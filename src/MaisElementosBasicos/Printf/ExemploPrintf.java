package MaisElementosBasicos.Printf;

public class ExemploPrintf {

    public static void main(String[] args) {

        System.out.printf("Hello %s", "Olá, Mundo!");
        System.out.println();
        System.out.printf("%S", "Olá, Mundo!");
        System.out.println();
        System.out.printf("%c", 'c');
    }
}
